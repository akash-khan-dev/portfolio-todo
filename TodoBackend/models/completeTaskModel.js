const mongoose = require("mongoose");
const { Schema, model } = mongoose;

const taskModel = new Schema({
  title: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  userId: {
    type: Schema.Types.ObjectId,
    ref: "User",
  },
});

module.exports = model("CompleteTask", taskModel);
